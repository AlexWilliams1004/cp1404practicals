import random

print(random.randint(5, 20))  # line 1
# smallest number should be 5, largest should be 20

print(random.randrange(3, 10, 2))  # line 2
# smallest number should be 3, largest should be 9

print(random.uniform(2.5, 5.5))  # line  3
# smallest number should be 2.5, largest should be 5.5

print(random.randint(1, 100))
# smallest number: 1, largest number: 100
