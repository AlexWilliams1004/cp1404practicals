import random

print(random.randint(5, 20))  # 14
# the smallest possible value is 5.
# the largest possible value was 20

print(random.randrange(3, 10, 2))  # 7
# smallest possible number is 3
# Largest possible number is 9

print(random.uniform(2.5, 5.5))  # 3.696444539925139
# The smallest possible value is 2.5
# The largest possible value is 5.499999999999999

print(random.randint(1, 100))
